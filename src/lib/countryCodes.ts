// Get India as default
export const getDefaultCountry = () => countryCodes.find(country => country.code === 'IN') || countryCodes[0]

// Format phone number with country code
export const formatPhoneNumber = (countryCode: string, phoneNumber: string) => {
    if (!phoneNumber) return ''
    return `${countryCode} ${phoneNumber}`
}

// Parse phone number to extract country code and number
export const parsePhoneNumber = (fullNumber: string) => {
    if (!fullNumber) return { countryCode: '+91', phoneNumber: '' }

    // Find the country code
    const country = countryCodes.find(c => fullNumber.startsWith(c.dialCode))
    if (country) {
        const phoneNumber = fullNumber.replace(country.dialCode, '').trim()
        return { countryCode: country.dialCode, phoneNumber }
    }

    // Default to India if no country code found
    return { countryCode: '+91', phoneNumber: fullNumber }
} 